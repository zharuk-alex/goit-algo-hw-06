    # Представлення графа за допомогою списку суміжності
    graph = {
        "A": ["B", "C"],
        "B": ["A", "D", "E"],
        "C": ["A", "F"],
        "D": ["B"],
        "E": ["B", "F"],
        "F": ["C", "E"],
    }

    # Запуск алгоритму BFS
    bfs_iterative(graph, "A")
    print("\n")

    # Запуск рекурсивного алгоритму BFS
    bfs_recursive(graph, deque(["A"]))
    print("\n")
